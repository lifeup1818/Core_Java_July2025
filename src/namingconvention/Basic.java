package namingconvention;

public class Basic {

	public static void main(String[] args) {
		
		System.out.println("*******boolean Type********");
		//boolean type
		boolean flag=false;
		boolean anotherFlag=true;
		
		System.out.println("flag:"+anotherFlag);
		
		System.out.println("*******char Type********");
		//char type
		char character='a';
		char anotherCharacter='A';
		System.out.println(character);
		System.out.println("anotherCharacter:"+anotherCharacter);
		
		System.out.println("*******Int Type********");
		//int type
		int value=400;
		int value1 = 0;
		
		System.out.println(value);
		System.out.println("value1:"+value1);
		
		System.out.println("*******byte Type********");
		byte abcd = 0;
		
		System.out.println("float type:"+abcd);
		
		//float type
		float floatValue=10.267838f;
		System.out.println("Float type:"+floatValue);
		
		System.out.println("*******double Type********");
		double dValue=10.27666;
		
		System.out.println("double type:"+dValue);
		
		System.out.println("*******long Type********");
		
		long lType=1747782131878798787L;
		
		System.out.println("ling type:"+lType);
		
	
		
	}
}
