package controlstatements;

public class ForLoop {

	public static void main(String[] args) {

		// print java 10 time
		for (int i = 0; i < 11; i++) {
			System.out.println("Java");
		}

		// print value 0 to 10
		for (int i = 0; i < 11; i++) {
			System.out.println(i);
		}

		// print value like 10,9,8,7......,0

		for (int i = 10; i >= 0; i--) {
			System.out.println(i);
		}

	}

}
