package operators;

public class RelationalOperator {
	
	public static void main(String[] args) {
		int a=100;
		int b=200;
		
		System.out.println("1. Equal to");
		System.out.println(a==b);
		
		System.out.println("2. Not Equal to");
		System.out.println(a!=b);
		
		System.out.println("3. Greater Than(>)");
		System.out.println("Check a value is greater than b or not:"+(a>b));
		
		System.out.println("4. Less Than(<)");
		System.out.println("Check a value is less than b or not:"+(a<b));
		
		System.out.println("5. Greater or equal(>=)");
		System.out.println("Check a value is greater than or equal to b:"+(a>=b));
		
		System.out.println("6. Less  or equal(<=)");
		System.out.println("Check a value is less than or equal to b:"+(a<=b));
		
		
		
	}

}
